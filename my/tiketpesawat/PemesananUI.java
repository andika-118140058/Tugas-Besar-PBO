    private void kelas_pesawatFrameActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_kelas_pesawatFrameActionPerformed
        if(kode_pesawatFrame.getSelectedItem().equals("MPT")){
            if(kelas_pesawatFrame.getSelectedItem().equals("Kelas 1")){
                objTiket.harga_tiket = 1500000;
            }else if(kelas_pesawatFrame.getSelectedItem().equals("Kelas 2")){
                objTiket.harga_tiket = 900000;
            }else if(kelas_pesawatFrame.getSelectedItem().equals("Kelas 3")){
                objTiket.harga_tiket = 500000;
            }
        }else if(kode_pesawatFrame.getSelectedItem().equals("GRD")){
            if(kelas_pesawatFrame.getSelectedItem().equals("Kelas 1")){
                objTiket.harga_tiket = 1200000;
            }else if(kelas_pesawatFrame.getSelectedItem().equals("Kelas 2")){
                objTiket.harga_tiket = 800000;
            }else if(kelas_pesawatFrame.getSelectedItem().equals("Kelas 3")){
                objTiket.harga_tiket = 400000;
            }
        }else if(kode_pesawatFrame.getSelectedItem().equals("BTV")){
            if(kelas_pesawatFrame.getSelectedItem().equals("Kelas 1")){
                objTiket.harga_tiket = 1000000;
            }else if(kelas_pesawatFrame.getSelectedItem().equals("Kelas 2")){
                objTiket.harga_tiket = 700000;
            }else if(kelas_pesawatFrame.getSelectedItem().equals("Kelas 3")){
                objTiket.harga_tiket = 300000;
            }
        }
    }//GEN-LAST:event_kelas_pesawatFrameActionPerformed

    private void kode_ruteFrameActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_kode_ruteFrameActionPerformed
        if(kode_ruteFrame.getSelectedItem().equals("TKG-CGK")){
            rute_pesawatFrame.setText("Lampung-Jakarta");
        }else if(kode_ruteFrame.getSelectedItem().equals("CGK-TKG")){
            rute_pesawatFrame.setText("Jakarta-Lampung");
        }else if(kode_ruteFrame.getSelectedItem().equals("TKG-DPS")){
            rute_pesawatFrame.setText("Lampung-Bali");
        }
        objRute.kode_rute = (String) kode_ruteFrame.getSelectedItem();
        objRute.rute_penerbangan = rute_pesawatFrame.getText();
    }//GEN-LAST:event_kode_ruteFrameActionPerformed

