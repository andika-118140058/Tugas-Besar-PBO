    private void biayaFrameActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_biayaFrameActionPerformed
        objTiket.jumlah_tiket = Integer.parseInt(jumlah_tiketFrame.getText());
        int harga = objTiket.harga_tiket;
        int jumlah = objTiket.jumlah_tiket;
        int hasil = harga*jumlah;
        total_biayaFrame.setText(Integer.toString(hasil));
    }//GEN-LAST:event_biayaFrameActionPerformed

    private void kembalianFrameActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_kembalianFrameActionPerformed
        objTiket.total_biaya = Integer.parseInt(total_biayaFrame.getText());
        objTiket.uang_bayar = Integer.parseInt(uang_bayarFrame.getText());
        int uang = objTiket.uang_bayar;
        int biaya = objTiket.total_biaya;
        int kembali = uang-biaya;
        uang_kembalianFrame.setText(Integer.toString(kembali));
    }//GEN-LAST:event_kembalianFrameActionPerformed

